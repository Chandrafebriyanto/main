//arrow function adalah fungsi yang ditulis dengan sintaksis yang lebih ringkas
//dan lebih mudah dibaca

// arrow deklarasi
function name3(){
    console.log('Hello World')
}
name3()

//arrow function
const name1 = () => {
    console.log('Hello World')
}
name1()

//sama dengan function expression
const name2 = function () {
    console.log('Hello World')
}
name2()
