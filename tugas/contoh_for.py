# Contoh perulangan for sederhana
for i in range(5):
    print(f"Ini adalah iterasi ke-{i+1}")

# Contoh perulangan for dengan list
buah = ["apel", "jeruk", "mangga", "pisang"]
for item in buah:
    print(f"Saya suka {item}")

# Contoh perulangan for dengan string
kata = "Python"
for huruf in kata:
    print(huruf)

# Contoh perulangan for dengan dictionary
siswa = {"nama": "John", "usia": 20, "jurusan": "Informatika"}
for key, value in siswa.items():
    print(f"{key}: {value}")